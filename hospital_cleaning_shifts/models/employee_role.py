from odoo import fields, models


class EmployeeRoll(models.Model):
    """To identify each employees job positions"""
    _name = 'employee.role'
    _description = 'employee_roles'

    job_position = fields.Many2one('hr.job', help="employees job positions")
    employee_id = fields.Many2many('hr.employee')
